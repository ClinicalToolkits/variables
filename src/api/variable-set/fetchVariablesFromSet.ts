import { VariableSet, Variable } from "../../types";
import { fetchVariables } from "../fetchVariable";

export async function fetchVariablesFromSet(variableSet: VariableSet): Promise<Variable[]> {
  const variableIds = variableSet.variableIds.all;
  const variableSuffix = variableSet.subversion ? variableSet.subversion : undefined;
  try {
    return await fetchVariables({
      variableIds,
      uniqueSuffix: variableSuffix,
      variableSetParams: {
        id: variableSet.id,
        key: variableSet.key,
        subversion: variableSet.subversion,
        descriptiveRatingId: variableSet.metadata?.descriptiveRatingId,
      },
      bIncludeAutoGeneratedVariables: true
    })
  } catch (error) {
    console.error(`Failed to fetch variables for variable set ${variableSet.key}:`, error);
    throw error;
  }
};

